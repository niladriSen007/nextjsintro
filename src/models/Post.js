import mongoose from "mongoose";

const { Schema } = mongoose;
mongoose.Promise = global.Promise;

const postSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      required: true,
    },
    img: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const PostDetails =  mongoose.model.PostDetail ||  mongoose.model("PostDetail", postSchema);